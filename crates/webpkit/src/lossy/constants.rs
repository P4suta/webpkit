//! VP8 vocabulary and constant tables (layer 0: usable everywhere).
//!
//! Every table here is transcribed verbatim from the libwebp reference decoder
//! (`src/dec/tree_dec.c`, `src/dec/quant_dec.c`, `src/dec/vp8_dec.c`) which is
//! itself RFC 6386. Keeping them in one place lets the bitstream and pixel
//! layers share exact constants and lets a reviewer diff them against the spec.
#![allow(
    clippy::unreadable_literal,
    reason = "these are fixed spec tables transcribed verbatim from RFC 6386 / \
              libwebp; digit grouping would obscure the diff against the source"
)]

/// An 8-bit probability that a coded boolean is zero, i.e. `prob / 256`, exactly
/// as VP8 stores and transmits it.
