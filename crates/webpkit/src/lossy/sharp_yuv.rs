//! Sharp (luminance-guided) chroma subsampling — the fixed-point, integer-only port
//! of libwebp's `libsharpyuv`, gated behind
//! [`LossyTuning::sharp_yuv`](crate::lossy::LossyTuning::sharp_yuv).
//!
//! The plain 4:2:0 path ([`crate::lossy::rgb_to_yuv`]) box-averages each chroma sample
//! from four *gamma-domain* RGB pixels. On saturated edges that averaging is wrong — a
//! half-bright/half-dark boundary should average in *linear* light — and the decoder's
//! bilinear chroma upsampling then bleeds the coarse sample across the edge, so the
//! reconstructed color drifts. Libwebp's `libsharpyuv` fixes both: it downsamples in a
//! linearized (gamma-decoded) space and iteratively adjusts the subsampled chroma so
//! that, once the decoder upsamples it against the *full-resolution* luma, the
//! reconstruction lands as close as possible to the original.
//!
//! This port keeps that structure but is deterministic and float-free:
//!
//! * **Gamma is a committed LUT.** [`GAMMA_TO_LINEAR`] maps an 8-bit gamma channel to a
//!   16-bit linear value and [`LINEAR_TO_GAMMA`] inverts it; both are generated once from
//!   libwebp's Rec.709-style transfer (`a = 0.0993`, `1/0.45` exponent) and committed as
//!   integers, so no `pow` / `float_arithmetic` runs at encode time. The pair round-trips
//!   exactly, so a flat region reproduces the plain-box chroma bit-for-bit.
//! * **The decoder is modeled by the real upsampler.** Each iteration reconstructs the
//!   frame with [`yuv420_to_rgba`] — the same fancy-upsampling path the decoder uses —
//!   against the fixed luma plane, so the refinement optimizes exactly what a decoder
//!   will produce.
//! * **Fixed iteration count.** [`SHARP_YUV_ITERATIONS`] (libwebp `kNumIterations`) Jacobi
//!   steps, each folding the linear-space color residual back into the chroma through the
//!   shared studio [`U_COEFF`] / [`V_COEFF`] matrix rows.
//!
//! Only the U and V planes are touched; the luma plane is the encoder's own and is left
//! unchanged, so decode self-consistency is automatic.
#![allow(
    clippy::cast_possible_truncation,
    clippy::cast_sign_loss,
    reason = "every value written to a plane is clamped to 0..=255 before the u8 cast and \
              every LUT index is masked into range, reproducing the reference byte handling"
)]

use crate::lossy::prelude::*;
use crate::lossy::rgb_to_yuv::{SourceYuv, U_COEFF, V_COEFF, YUV_FIX, rgb_to_u, rgb_to_v};
use crate::lossy::yuv::{Yuv420Ref, yuv420_to_rgba};

/// The number of luma-guided refinement passes (libwebp `kNumIterations`). Each pass is a
/// Jacobi correction toward the decoder-reconstructed color; four is libwebp's fixed count.
const SHARP_YUV_ITERATIONS: usize = 4;
/// Right shift of the linear-value LUT index: a 16-bit linear value addresses
/// [`LINEAR_TO_GAMMA`]'s `4096` buckets through `linear >> LG_SHIFT`.
const LG_SHIFT: u32 = 4;

/// Descale a signed fixed-point accumulator by `YUV_FIX`, rounding to nearest and
/// symmetric about zero so a positive and negative residual of equal magnitude move the
/// chroma by the same step.
const fn round_shift(acc: i32) -> i32 {
    let half = 1 << (YUV_FIX - 1);
    if acc >= 0 {
        (acc + half) >> YUV_FIX
    } else {
        -(((-acc) + half) >> YUV_FIX)
    }
}

/// Gamma-decode one 8-bit channel to its 16-bit linear value.
fn to_linear(gamma: u8) -> i32 {
    i32::from(GAMMA_TO_LINEAR[usize::from(gamma)])
}

/// Gamma-encode a 16-bit linear value (clamped to `0..=65535`) back to 8 bits.
fn to_gamma(linear: i32) -> u8 {
    LINEAR_TO_GAMMA[(linear.clamp(0, 65535) >> LG_SHIFT) as usize]
}

/// The studio U (or V) chroma of a single gamma RGB representative. The plain path's
/// [`rgb_to_u`] / [`rgb_to_v`] fold a `/4` into their descale (they sum a 2×2 block), so a
/// single representative feeds `4 ×` each channel to reuse the exact coefficients and
/// rounding — keeping this chroma in the same space the decoder inverts.
fn cell_u(cell: Cell) -> u8 {
    rgb_to_u(
        4 * i32::from(cell.r),
        4 * i32::from(cell.g),
        4 * i32::from(cell.b),
    )
}

/// The studio V chroma of a single gamma RGB representative (see [`cell_u`]).
fn cell_v(cell: Cell) -> u8 {
    rgb_to_v(
        4 * i32::from(cell.r),
        4 * i32::from(cell.g),
        4 * i32::from(cell.b),
    )
}

/// One chroma cell's gamma-domain RGB representative: the linear-space average of the four
/// covering source pixels, re-encoded to gamma. This is the "correct" downsampled color —
/// the target the refinement drives the reconstruction toward.
#[derive(Clone, Copy)]
struct Cell {
    /// Gamma red.
    r: u8,
    /// Gamma green.
    g: u8,
    /// Gamma blue.
    b: u8,
}

/// A borrowed RGBA source with its true (unpadded) dimensions and an edge-replicating
/// pixel fetch, so chroma cells covering the padded macroblock border read real samples.
struct Source<'a> {
    /// The picture, 4 bytes/pixel, row-major.
    rgba: &'a [u8],
    /// Picture width in pixels.
    width: usize,
    /// Picture height in pixels.
    height: usize,
}

impl Source<'_> {
    /// The gamma RGB of pixel `(x, y)`, clamped to the picture bounds (VP8 edge extension).
    fn rgb(&self, x: usize, y: usize) -> (u8, u8, u8) {
        let cx = x.min(self.width - 1);
        let cy = y.min(self.height - 1);
        let i = (cy * self.width + cx) * 4;
        (self.rgba[i], self.rgba[i + 1], self.rgba[i + 2])
    }

    /// The linear-average gamma RGB [`Cell`] of the 2×2 block a chroma sample covers.
    fn target_cell(&self, cx: usize, cy: usize) -> Cell {
        let (mut lr, mut lg, mut lb) = (0i32, 0i32, 0i32);
        for dy in 0..2 {
            for dx in 0..2 {
                let (r, g, b) = self.rgb(2 * cx + dx, 2 * cy + dy);
                lr += to_linear(r);
                lg += to_linear(g);
                lb += to_linear(b);
            }
        }
        Cell {
            r: to_gamma((lr + 2) >> 2),
            g: to_gamma((lg + 2) >> 2),
            b: to_gamma((lb + 2) >> 2),
        }
    }
}

/// Replace `src`'s U and V planes with luminance-guided (sharp) chroma. The luma plane and
/// the padded macroblock geometry are unchanged; `rgba` is the original picture
/// (`width` × `height`, 4 bytes/pixel) the luma was derived from.
///
/// The plane layout matches [`crate::lossy::rgb_to_yuv::from_rgba`]: `src.y` is
/// `mb_w*16 × mb_h*16` and the chroma planes are `mb_w*8 × mb_h*8`, edge-extended past the
/// picture. Cells with no in-picture pixel keep their linear-average target (the border
/// replication) and are never iterated, since the decoder never reconstructs them.
pub(crate) fn refine_chroma(rgba: &[u8], width: usize, height: usize, src: &mut SourceYuv) {
    if width == 0 || height == 0 {
        return;
    }
    let source = Source {
        rgba,
        width,
        height,
    };
    let cw = src.uv_stride();
    let ch = src.mb_h * 8;
    let y_stride = src.y_stride();

    // The per-cell target color (constant across iterations) and the working chroma, seeded
    // to the chroma of that target — i.e. the gamma-correct box downsample before any
    // upsampling compensation.
    let mut target = Vec::with_capacity(cw * ch);
    for cy in 0..ch {
        for cx in 0..cw {
            let cell = source.target_cell(cx, cy);
            src.u[cy * cw + cx] = cell_u(cell);
            src.v[cy * cw + cx] = cell_v(cell);
            target.push(cell);
        }
    }

    for _ in 0..SHARP_YUV_ITERATIONS {
        // Reconstruct exactly as the decoder would: fancy-upsample the current chroma over
        // the fixed full-resolution luma. Comparing this to the source in linear light and
        // folding the residual back into the chroma is one Jacobi step of the deconvolution.
        let recon = yuv420_to_rgba(
            &Yuv420Ref {
                y: &src.y,
                y_stride,
                u: &src.u,
                v: &src.v,
                uv_stride: cw,
            },
            width,
            height,
        );
        for cy in 0..ch {
            for cx in 0..cw {
                let idx = cy * cw + cx;
                let Some((rr, rg, rb)) = recon_cell(&recon, width, height, cx, cy) else {
                    continue;
                };
                let t = target[idx];
                let dr = i32::from(t.r) - i32::from(rr);
                let dg = i32::from(t.g) - i32::from(rg);
                let db = i32::from(t.b) - i32::from(rb);
                let du = round_shift(U_COEFF[0] * dr + U_COEFF[1] * dg + U_COEFF[2] * db);
                let dv = round_shift(V_COEFF[0] * dr + V_COEFF[1] * dg + V_COEFF[2] * db);
                src.u[idx] = (i32::from(src.u[idx]) + du).clamp(0, 255) as u8;
                src.v[idx] = (i32::from(src.v[idx]) + dv).clamp(0, 255) as u8;
            }
        }
    }
}

/// The reconstructed cell's gamma RGB: the linear-space average of the reconstructed
/// picture pixels the chroma sample covers, re-encoded to gamma. `None` for a cell wholly
/// outside the picture (a padded border sample the decoder never emits).
fn recon_cell(
    recon: &[u8],
    width: usize,
    height: usize,
    cx: usize,
    cy: usize,
) -> Option<(u8, u8, u8)> {
    let (mut lr, mut lg, mut lb, mut n) = (0i32, 0i32, 0i32, 0i32);
    for dy in 0..2 {
        let py = 2 * cy + dy;
        if py >= height {
            continue;
        }
        for dx in 0..2 {
            let px = 2 * cx + dx;
            if px >= width {
                continue;
            }
            let o = (py * width + px) * 4;
            lr += to_linear(recon[o]);
            lg += to_linear(recon[o + 1]);
            lb += to_linear(recon[o + 2]);
            n += 1;
        }
    }
    if n == 0 {
        return None;
    }
    let half = n / 2;
    Some((
        to_gamma((lr + half) / n),
        to_gamma((lg + half) / n),
        to_gamma((lb + half) / n),
    ))
}

